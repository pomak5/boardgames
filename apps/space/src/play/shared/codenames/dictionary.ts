/** Случайная выборка `count` слов без повторов. */
export function pickWords(
  count: number,
  random: () => number = Math.random,
  dictionary: readonly string[] = CODENAMES_WORDS_RU,
): string[] {
  if (count > dictionary.length) {
    throw new Error(`В словаре только ${dictionary.length} слов, запрошено ${count}`);
  }
  const pool = [...dictionary];
  for (let i = pool.length - 1; i > 0; i--) {
    const j = Math.floor(random() * (i + 1));
    [pool[i], pool[j]] = [pool[j] as string, pool[i] as string];
  }
  return pool.slice(0, count);
}
